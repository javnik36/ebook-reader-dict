def sanitize_text(text):
    result = text
    result = re.sub(r"&#39;", "'", result)
    result = re.sub(r"&quot;", "\"", result)
    result = re.sub(r"&amp;", "&", result)
    return result

def transliterate(parts: List[str] #'uk','słowo'
                  )-> str:
    '''
    Returns transliterated version of string.
    >>> transliterate(["uk","йолоп"])
    jolop
    >>> transliterate(["ru","уникальный"])
    unikalʹnyj
    ''' #noqa
    
    language = parts[0]
    text = sanitize_text(parts[1])
    transliteration = transliterations.get(language)

    if transliteration is None:
        raise ValueError(f"(translit): Błędny kod języka: {language}")

    result = ''

    for codepoint in map(ord, text):
        if codepoint == 0xbcd:
            if result and result[-1] == 'A':
                result = result[:-1]
        else:
            untransliterated = chr(codepoint)
            transliterated = commonTransliteration.get(untransliterated)
            
            if transliterated is None:
                transliterated = transliteration.get(untransliterated)
            
            if transliterated is None:
                return f"(translit): Nieprawidłowy znak {untransliterated} dla języka o kodzie {language}."
            else:
                if language == 'tm' and transliterated[-1] != 'A' and transliterated != ' ' and result and result[-1] == 'A':
                    result = result[:-1]
                result += transliterated

    if language == 'tm':
        result = re.sub(r'A', 'a', result)
        result = re.sub(r'ḵs', 'x', result)
        result = re.sub(r'ḵp', 'f', result)
        result = re.sub(r'ḵj', 'z', result)

    return result